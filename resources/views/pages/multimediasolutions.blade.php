            <h3>Uso de dispositivos</h3>
            <p>Algunos de los dispositivos y sistemas que se presentaron se pueden encontrar en el mercado, los
                siguientes puntos pueden ejecutarse si el musicoterapeuta tiene disponibilidad de estos
                recursos:</p>
            <p></p>
            <p>Permitirle al paciente escuchar al paciente música de su preferencia usando dispositivo de
                conducción ósea para mayor estimulación en rutinas de tiempo controlado.</p>
            <p>Realizar ejercicios de asociación de color y sonido. Por ejemplo: utilizar una canción de su
                preferencia y presentarle un objeto con un color característico. En este punto sería importante
                el uso de sistemas parecidos a los de la cámara sensorial cromo-acústica, que presenta al
                paciente imágenes, videos y colores que se muestran junto con la música y además el componente
                de vibración. Este procedimiento debe ser regulado y muy estudiado.</p>
            <p></p>
            <br>
            <h3>Musical</h3>
            <br>
            <ul class="lista-parrafo">
                <li>Recopilar en lo posible información de la música que el paciente escuchaba en diferentes
                    etapas de su vida, si el paciente tiene dificultades para ofrecer información puede hacerse
                    consultas a sus familiares, amigos o contemporáneos.</li>

                <li>Asociar melodías a actividades de su rutina diaria, es decir, que se reproduzca una melodía
                    fácil de recordar antes de iniciar una actividad, que funcione como un anuncio. Los sonidos
                    de una melodía fácil de recordar deben ser consonantes, es decir que resulten agradables y
                    resueltos. Interpretados con instrumentos cuyo timbre no resulte grotesco. Puede ser el
                    sonido de un piano, guitarra, flauta o kalimba.</li>

                <li>Estimular otros sentidos como el olfato y asociarse a canciones o melodías que se usen en su
                    tratamiento. El sentido del olfato puede acceder más rápido al cerebro y a la memoria.</li>

                <li>Realizar actividades lúdicas donde se usen dispositivos con aplicaciones musicales.</li>

                <li>Realizar sesiones de ejercicio usando instrumentos rítmicos fáciles de hacer sonar como
                    maracas, claves, tambores, y melódicos como flautas, melódicas o kalimbas.</li>

                <li>Realizar sesiones de canto de forma individual o grupal procurando elegir canciones que
                    vayan con sus preferencias musicales.</li>

                <li>Enseñar al paciente la ejecución de un instrumento musical fácil de ejecutar procurando que
                    la metodología sea muy accesible.</li>

                <li>Monitorear los resultados de la aplicación de estos procesos permanentemente</li>
            </ul>
            <br>
            <h3>Consideraciones</h3>
            <p>La enfermedad de Alzheimer aparece con más frecuencia en etapas avanzadas de la vida, no tiene cura y progresa con el tiempo. Los planes de musicoterápia contienen fases de recopilación de datos del paciente en donde se incluye registrar preferencias musicales entre otras cosas. (Gomez garcia et.at)</p>
            <p>Las personas escuchamos todo tipo de música durante nuestra vida, algunos de estas canciones u obras se pierden en el tiempo pero permanecen en nuestra memoria y esa escencia se vuelve a percibir cuando reconocemos dichas canciones. Una solución a esto podría ser, registrar los datos de canciones u obras musicales relevantes para nosotros y guardarla en un apartado de datos personalizado y seguro al que podamos ingresar libremente. </p>
