            <h3>Estudios con procedimientos variados</h3>
            <p>En el artículo <i>Beneficios de la musicoterapia en las relaciones conductuales</i> se hace una
                recopilación de investigaciones y sus resultados con respecto a la aplicación de la terapía
                musical aplicada como canto de música preferida grupal, lectura, y actividades interactivas. en
                distintos grupos de pacientes. Se destacan entre ellos: <br><br></p>


            <p>Sakamoto et al, en 2013 realizaron una investigación implementando la escucha de música pasiva y
                actividades interactivas en pacientes con Alzheimer severo una vez cada semana durante diez
                semanas, los resultados mostraron una mejoría a corto plazo donde mejoró su estado de ánimo
                después de la intervención, a largo plazo se evidenció mejoría en las relaciones afectivas y
                redujeron las fobias. <br><br></p>

            <p>M. L. Cooke et al. en 2010 realizaron una investigación en la que aplicaron la musicoterapia y la
                lectura a 47 participantes con Alzheimer, se hizo en sesiones de 10 minutos durante 8 semanas
                evaluando las alteraciones de conducta y la ansiedad. Los resultados mostraron bajos niveles de
                ansiedad y disminución de los comportamientos agresivos. <br><br></p>

            <p>M.Zare et al. 2010 fue un estudio en el que se comparó estadísticamente resultados de aplicar
                actividades distintas a cuatro grupos de personas con Alzheimer. Los resultados fueron
                disminución de la agitación, pero los datos recogidos no se pueden generalizar. <br><br></p>

            <p>Hay que recalcar que estas investigaciones no se hicieron bajo un estándar definido como el que
                se menciona, además en su mayoría fueron realizadas por terapeutas ocupacionales y músicos. Las
                herramientas musicales usadas en la mayoría de los estudios son de la cultura occidental por lo
                que los datos obtenidos no pueden generalizarse a otras regiones por diferencias culturales,
                Además, los datos se obtuvieron de poblaciones pequeñas por lo que es necesario ampliar los
                tamaños de las muestras para hacer una generalización. <br><br></p>

            <p>La terapia musical en el ámbito de las ciencias de la salud es un campo en desarrollo del que no
                existe mucha documentación respecto a investigaciones, en este sentido hay una gran oportunidad
                de incluir nuevos métodos y evaluar sus resultados en pacientes.</p>
