            <h3>Audífonos o altavoces de conducción ósea</h3>
            <p>Las vibraciones sonoras viajan por el aire hasta las vías de oído externo donde inicia el proceso
                fisiológico de conversión de la información a pulsos eléctricos que viajan hacia el cerebro y
                son interpretados.</p><br>
            <p>Los audífonos de conducción ósea no transmiten el sonido a través del aire, sino que trasmiten
                vibraciones que contienen la información de los sonidos a través de la piel. Los pulsos
                vibratorios se propagan por la piel y los huesos hasta llegar al oído interno. </p><br>
            <p>El audífono está controlado con un panel de control que regula su comportamiento.</p><br>
            <p>Este dispositivo podría ayudar a escuchar mejor a los pacientes que presenten deterioro auditivo,
                pero también puede probarse como herramienta en sesiones de musicoterapia.</p><br><br>

            <h3>Cámara sensorial cromo musical armonizadora interactiva</h3>
            <p>Esta cámara es un sistema enfocado a la reducción del estrés usando la cromoterapia y la música.
                La cromoterapia usa los colores y la luz para realizar como terapia para reducir los síntomas de
                algunas enfermedades. Las notas musicales pueden representarse como colores, y adicionalmente la
                silla vibra según sea el color que se muestre, por lo que este dispositivo combina el color, el
                sonido y el tacto</p>
            <p>El uso de este sistema es de carácter terapéutico.</p>
            <p>Este sistema está compuesto por varios terminales con muchas características:</p><br><br>

            <p class="content-tab">
                Un sillón vibratorio y giratorio: que vibra dependiendo del color o la música que se esté
                reproduciendo. La silla está controlada por un terminal de control en donde se encuentra una
                pantalla que reproduce videos e imágenes relacionadas con la música. Como se mencionó en el
                trabajo de (Gomez garcia y Gomez gallego) la música y las imágenes podrían están enfocadas a los
                gustos de
                paciente.
            </p>

            <h3>Silla y sistema para transmitir sonido y vibración</h3>
            <p>
                Este es un sistema parecido al anterior, se trata de una silla que transmite sonido y vibración
                desde unos parlantes y dispositivos de vibración instalados en el interior del sillón. También
                permite inclinar y declinar la silla. El control de los impulsos e inclinación se encuentra los
                brazos del sillón.
            </p><br>
            <p>Lo novedoso de este sistema es que cuenta con un software con que se puede controlar más
                específicamente los niveles de vibración y de sonido. Tiene un reproductor de música integrado
                que
                pueden configurarse al gusto del usuario y cuenta con controles de ecualización y
                configuraciones para los parlantes integrados.</p><br>

            <h3>Dispositivo lara estimular la percepción sensorial.</h3>
            <p>Este sistema es similar al anterior: consiste en la transmisión de vibraciones sonoras y sirve
                para estimular la percepción sensitiva de los pacientes con demencia. Consta de dos terminales
                emisoras, una terminal de control, y un colchón de acoplamiento o de agua para facilitar la
                trasmisión de vibraciones.</p>
