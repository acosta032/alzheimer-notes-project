            <h3>Estudios</h3>
            <p>Un referente del seguimiento de estas fases es una investigación hecha hecho por (Gomez garcia y
                Gomez gallego) en el que seleccionaron una muestra poblacional de pacientes de dos centros
                residenciales en Murcia, España. Y que se muestran a continuación: <br><br></p>

            <ol class="lista-parrafo content-left">
                <li>Solicitud el consentimiento de los familiares de los pacientes para incluirlos en el
                    programa de rehabilitación y el comité de ética de su alma mater dio luz verde a su
                    ejecución.<br></li>
                <li>Recopilación de gustos musicales del paciente, registro de su estado mental, registro de
                    comportamientos y de tendencia hacia depresión.<br></li>
                <li>La ejecución del plan consistió en la exposición de la música escogida en el registro la
                    cual se reprodujo desde un equipo de sonido durante 45 minutos cada dos semanas. Esta rutina
                    se llevó a cabo junto con danzaterapia consistente en la ejecución de instrumentos como
                    maracas al ritmo de la música. La recopilación de datos terminó en la sexta semana desde la
                    cual se inicia el análisis.<br></li>
                <li>Los resultados mostraron:</li>
                <ul>
                    <li>Disminución de conductas como el delirios, alucinaciones, irritabilidad y agitación.<br>
                    </li>
                    <li>La musicoterapia aumenta el umbral de tolerancia ante estímulos ambientales estresantes.
                    </li>
                    <li>Mejora las relaciones sociales lo que mejora el estado de ánimo.</li>
                    <li>Cuando la música se adapta a las preferencias personales evoca emociones positivas. La
                        escucha de melodías familiares hace que los pacientes evoquen recuerdos autobiográficos
                        y las emociones asociadas a ellos.<br><br></li>
                </ul>
                <li>Se concluye que la terapia de la música puede tener beneficios estimulantes sobre la
                    capacidad de aprendizaje y del ánimo, así como también puede servir como un inhibidor de
                    conductas desencadenadas por el estrés como a irritabilidad.</li>
            </ol>
            <p>Los participantes de la investigación incluyeron a dos musicoterapeutas profesionales. <br><br>
