            <h3>Componentes de la música</h3>
            <h4>Espectro audible</h4>
            <p>El especto audible es el campo de frecuencias sonoras que pueden ser captadas por el oído humano.
                La unidad de medida de la frecuencia es el Hercio su abreviatura es Hz.</p><br>

            <a href="/img/3_espectro.png"><img src="/img/3_espectro.png" alt="Espectro audible"
                    class="mx-auto d-block img-fluid"></a>

            <p>La música occidental está basada en su mayoría en la afinación estándar llamada
                <strong>Temperamento igual</strong>. El temperamento igual es un sistema de afinacón que divide
                el espectro audible en secciones llamadas octavas. A su vez, una <strong>octava</strong> esta
                dividida en doce partes llamadas semitonos. El mapa de frecuencias está descrito por un
                <strong>modelo logaritmico.</strong>
            </p><br>

            <a href="/img/3.1_octavas.png"><img src="/img/3.1_octavas.png" alt="Partes del espectro"
                    class="mx-auto d-block img-fluid"></a>


            <p>Para la composición musical suelen usarse las primeras 8 octavas cuyos sonidos son perceptibles
                cuando se combinan con otros además son sonidos que el oído considera tolerables. Los sonidos
                muy agudos son dañinos para tímpano en el oído y para las bocinas del dispositivo que las
                reproduce, en cambio los sonidos muy graves suelen sonar muy despacio, su sonido algunas veces
                puede resultar confuso.</p>
            <br>
            <p>Los doce semitonos representados en una recta <strong>son las distancias que hay entre una
                    frecuencia y otra</strong>, esta recta puede entenderse un <em>metro</em> en el que entre el
                centímetro y centímetro se encuentran divisiones más pequeñas, aunque las frecuencias obedecen a
                un modelo no lineal.</p>
            <br>
            <p>Cada graduación es una frecuencia que representa la <strong>altura</strong> de un sonido, es
                decir que tan grave o agudo es.</p>
            <br>


