            <h3>Consonancias y disonancias</h3>
            <br>
            <h4>Consonancia:</h4>
            <p>es una cualidad del sonido en el que algunas notas musicales al ejecutarse al mismo tiempo o uno
                seguido del otro el oído percibe como agradable, resuelto y coherente.</p>
            <p>Por ejemplo: Cuando dos notas separadas por una distancia de una 4J o 5J se ejecutan al mismo
                tiempo, el sonido es consonante</p>
            <br>
            <h4>Disonancia:</h4>
            <p>es una noción subjetiva según la cual algunos intervalos musicales suenan incoherentes, tenso,
                confuso o poco agradable, esto significa que cuando algunas notas musicales se ejecutan al mismo
                tiempo o uno seguido del otro el oído puede percibirlos como “chocante” o poco resuelto.</p>
            <p>Por ejemplo: Cuando dos notas separadas por una distancia de una 2m o 7M se ejecutan al mismo
                tiempo, el sonido resulta disonante.</p>
            <br>

            <p><strong>Melodía</strong>: es una sucesión de sonidos que se percibe como una sola entidad se
                desarrolla a través del tiempo y posee un carácter e identidad y significado en un contexto
                sonoro.</p>
            <br>
            <br>

            <h3>Armonía</h3>
            <p>La armonía s el estudio de la forma como enlazar grupos de sonidos llamados <strong>acordes.
                </strong>
                Un <strong>Acorde</strong> es un grupo de 3 o más sonidos que se suenan simultáneamente.

            </p><br>


            <br>

            <p><strong>Tipos de acordes:</strong></p>
            <p><strong>Acorde mayor</strong>: es una acorde conformado por la superposición de notas separadas
                por 3M y 3m.</p>
            <br>
            <a href="/img/3.11_acorde_mayor.png"><img src="/img/3.11_acorde_mayor.png" alt="Acorde mayor"
                    class="mx-auto d-block img-fluid"></a>

            <p>Los acordes mayores suelen tener un sonido que evoca resolución, armonía, y suele usarse en
                canciones alegres como por ejemplo en la música infantil. Pero en general se usan en todo tipo
                de música ya sea independiente o no de la letra.</p>
            <br>

            <p><strong>Acorde menor</strong>: es una acorde conformado por la superposición de notas separadas
                por 3m y 3M.</p>
            <br>
