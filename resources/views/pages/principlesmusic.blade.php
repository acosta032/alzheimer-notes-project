            <p><strong>Tonalidad</strong></p>
            <p>En música la tonalidad es la organización de una sucesión de notas que tienen una relación
                directa con una nota principal a la que llamamos tónica. La tónica puede ser cualquiera de las
                doce notas musicales desde Do hasta Si y respecto de esta se organizarán las demás notas de
                acuerdo a una escala mayor o menor.</p>
            <br>
            <a href="/img/3.14_tabla_tonalidades.png"><img src="/img/3.14_tabla_tonalidades.png"
                    alt="Tabla de 12 tonalidades" class="mx-auto d-block img-fluid"></a>
            <br>
            <p>La tonalidad escogida para componer una canción depende de factores como el rango de la voz del
                cantante, o el rango de notas de la instrumentación, la dificultad para interpretar la canción
                en determinado instrumento o la personalidad que se le quiera dar a la canción.</p>
